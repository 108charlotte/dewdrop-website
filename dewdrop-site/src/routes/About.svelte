<p>
    <span class="dewdrop">Dewdrop</span> is an Atlanta days of service event taking place in March, 2026. 
    During this two-day event, participants will create a project of their choosing while attending workshops and participating in fun activities. 
    This event is geared towards beginners but those with more experience are also welcome! 
    We'll conclude with a project showcase and a networking dinner with women in STEM fields! 
    <br><br>
    We're affiliated with <a class="hackclub" href="https://hackclub.com/">Hack Club</a>, a global organization backed by Dell and GitHub and registered 501(C)(3), through its <a class="athena" href="https://athena.hackclub.com/">Athena initiative</a>, whose goal is to close the gender gap in STEM fields. 
    
